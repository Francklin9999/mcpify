export interface ExtensionFile {
  path: string;
  content: string;
}

/** The materialized extension files (manifest + worker + readme), with the bridge port baked into the worker. */
export function extensionFiles(port: number = DEFAULT_PORT, version: string = EXTENSION_VERSION): ExtensionFile[] {
  return [
    { path: "manifest.json", content: MANIFEST(version) },
    { path: "background.js", content: BACKGROUND.replace("__PORT__", String(port)) },
    { path: "README.txt", content: READMETXT(port) },
  ];
}

/** Write the extension to disk (default ~/.urlmcp/extension) and return where it landed. */
export function installExtension(destDir?: string, port: number = DEFAULT_PORT): { dir: string; files: string[] } {
  const dir = destDir || join(forgeHome(), "extension");
  mkdirSync(dir, { recursive: true });
  const files = extensionFiles(port);
  for (const f of files) writeFileSync(join(dir, f.path), f.content);
  return { dir, files: files.map((f) => f.path) };
}
